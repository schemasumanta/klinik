			let by_hari_ini_nifas=$('#by_hari_ini_nifas').prop('checked');

			let tanggal_awal_nifas='';
			let tanggal_akhir_nifas='';

			if (by_hari_ini_nifas==true) {
				var date = new Date();
				var day = date.getDate();
				var month = date.getMonth() + 1;
				var year = date.getFullYear();
				if (month < 10) month = "0" + month;      
				if (day < 10) day = "0" + day;
				tanggal_awal_nifas=year + "-" + month + "-" + day;
				tanggal_akhir_nifas=year + "-" + month + "-" + day;


			}else{
				tanggal_awal_nifas=$('#tanggal_awal_nifas').val();
				tanggal_akhir_nifas=$('#tanggal_akhir_nifas').val();
			}




			if (tanggal_awal_nifas=='') {
				alert('Tanggal Awal Tidak Boleh Kosong');
				return false
			}

			if (tanggal_akhir_nifas=='') {
				alert('Tanggal Akhir Tidak Boleh Kosong');
				return false
			}

			if (tanggal_akhir_nifas<tanggal_awal_nifas) {
				alert('Tanggal Akhir Harus Lebih Besar dari Tanggal Awal');
				return false
			}

